package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Penjumlahan:");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("5 + 1 = " + hasilPenjumlahan + "\n");

        System.out.println("Pengurangan:");
        int hasilPengurangan = 5 - 1;
        System.out.println("5 - 1 = " + hasilPengurangan + "\n");

        System.out.println("Perkalian:");
        int hasilPerkalian = 5 * 2;
        System.out.println("5 * 2 = " + hasilPerkalian + "\n");

        System.out.println("Pembagian:");
        int hasilPembagian = 10 / 2;
        System.out.println("10 / 2 = " + hasilPembagian + "\n");

        System.out.println("Sisa Bagi:");
        int hasilSisa = 10 % 3;
        System.out.println("10 % 3 = " + hasilSisa + "\n");
    }
}
